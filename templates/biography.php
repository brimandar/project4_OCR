    <h1 class="mb-4">Ma Biographie</h1>

    <div>
        <img class="float-right" src="/img/lunettes.jpg" alt="Image d'un livre avec une paire de lunettes">

        <p>De roman en roman, Jean FORTEROCHE a noué un lien unique avec les lecteurs.</p>
        <br>
        <p>Né en 1974 à La Rochelle, il se prend de passion pour la littérature très jeune, 
            consacrant tout son temps libre à dévorer des livres dans la bibliothèque municipale 
            où travaille son oncle. C’est grâce à un concours de nouvelles proposé par son 
            professeur de français qu’il découvre le bonheur de l’écriture. 
            À compter de ce jour, et jusqu’à aujourd’hui, il ne cessera plus de noircir des 
            carnets. 
        <br>
        <p>Ses études, ses nombreux voyages à l'étranger, ses rencontres, tout vient enrichir 
            son imagination et ses projets de roman. Diplômé de langues, il devient photo reporters 
            et parcours le monde. En 2001, il publie son premier roman, <em>L'éclair de Thor</em>, mais c’est 
            le suivant, <em>Les légendes de Svenn</em> qui consacre sa rencontre avec le public. 
            Cette  histoire d'amitié et de suspense soulignée de surnaturel lui vaut un succès 
            fulgurant qui ne se démentira plus. Traduits en quarante langues, plusieurs fois 
            adaptés au cinéma, tous ses livres connaissent un immense succès en France et dans 
            le monde. Pour les lecteurs, chaque nouveau roman de Jean FORTEROCHE est désormais 
            un événement et un rendez-vous.
        </p>
